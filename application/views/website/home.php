        <h1>RSPB SUMATRA, INDONESIA: Detecting forest degradation & key tree species from satellites or drones</h1>
        <p>The Harapan rainforest project was set up in 2007 by the RSPB, BirdLife International and Burung Indonesia to protect some of the last of Sumatra's lowland forest, 95% of which has been destroyed since 1990. Harapan is home to tigers, Asian elephants, otters, porcupines, bears and turtles, and it stores an enormous amount of carbon. However, Harapan is under serious pressure from illegal logging and encroachment. Forest loss is easy to detect from satellite and drone images. Degradation, however, requires a trained eye but if anything its more important to spot so that the problem can be addressed before the rest is logged. We currently use satellite imagery and have one drone which can take high resolution pictures a few hectares at a time.. We need to be able to process this data and identify degradation, and a 5 key tree species which indicate the health of the forest, as fast as possible.</p>
